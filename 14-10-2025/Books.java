class Books
{
	public static void main(String[] args)
	{
		double a=1800; //total Books
		double b=40; //math Books
		double c=20; // english Books
		double d=30; //science Books
		double e=10; //social Books
		double f=a*(b/100.0);
		double g=a*(c/100.0);
		double h=a*(d/100.0);
		double i=a*(e/100.0);
		
		System.out.println("Math Books:"+f);
		System.out.println("English Books:"+g);
		System.out.println("Science Books:"+h);
		System.out.println("Social Books:"+i);
	}
}