class IfConditions
{
	public static void main(String[] args)
	{
		int mark=49;
		
		if(mark>=91 && mark<=100)
		{
			System.out.println("A");
		}
		else if(mark>=71&& mark<=90)
		{
			System.out.println("B");
		}
		else if(mark>=51&& mark<=70)
		{
			System.out.println("C");
		}
		else
		{
			System.out.println("D");
		}
		
	}
}